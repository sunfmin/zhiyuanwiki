package zj

import "testing"

func TestMajorIdent(t *testing.T) {
	tests := []struct {
		name, remark, want string
	}{
		// 命名方向并入专业名（取首括号）
		{"工科试验班", "（信息）（含计算机科学与技术、信息安全、软件工程）", "工科试验班（信息）"},
		{"工科试验班", "（竺可桢学院图灵班）（含计算机科学与技术专业）", "工科试验班（竺可桢学院图灵班）"},
		{"工科试验班", "（海洋，含智慧海洋卓越班）（含海洋工程与技术）", "工科试验班（海洋，含智慧海洋卓越班）"},
		{"工科试验班", "（材料、化工与高分子）含材料科学与工程", "工科试验班（材料、化工与高分子）"},
		// 「（含…）」是包含专业的说明、非方向 → 视为基础大类，不加后缀
		{"工科试验班", "（含车辆工程、过程装备与控制工程、能源与环境系统工程）", "工科试验班"},
		// 无备注 / 无括号 → 原样
		{"计算机科学与技术", "", "计算机科学与技术"},
		{"市场营销", "奢侈品营销方向", "市场营销"},
		// 普通方向括号（非大类）也会保留——浙江专业平行志愿下本就是独立单位
		{"市场营销", "（奢侈品营销方向）", "市场营销（奢侈品营销方向）"},
	}
	for _, tt := range tests {
		if got := majorIdent(tt.name, tt.remark); got != tt.want {
			t.Errorf("majorIdent(%q,%q) = %q, want %q", tt.name, tt.remark, got, tt.want)
		}
	}
}
