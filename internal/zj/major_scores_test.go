package zj

import "testing"

func TestParseMajorScoreRows(t *testing.T) {
	rows := [][]string{
		{"年份", "院校名称", "院校代码", "科类", "批次", "专业", "专业代码", "所属专业组", "专业备注", "选科要求", "录取人数", "最低分数", "最低位次", "学校所在", "学校性质", "是否985", "是否211"},
		{"2025", "浙江大学", "0001", "综合", "普通类一段", "计算机科学与技术", "01", "", "", "物理、化学(2科必选)", "30", "660", "1200", "浙江", "公办", "是", "是"},
		{"2023", "某校", "0002", "综合", "平行录取二段", "旅游管理", "02", "", "", "不限", "10", "480", "180000", "海南", "民办", "否", "否"},
		// 艺术类应被科类过滤掉
		{"2025", "某美院", "0003", "艺术类", "艺术类本科批", "美术", "03", "", "", "不限", "5", "560", "9999", "浙江", "公办", "否", "否"},
		// 高校专项计划批不在收录批次内
		{"2025", "某校", "0004", "综合", "高校专项计划批", "护理学", "04", "", "", "化学必选", "5", "550", "30000", "江苏", "公办", "否", "否"},
		// 位次缺失应被排除
		{"2025", "无位次校", "0005", "综合", "普通类一段", "测试", "05", "", "", "不限", "1", "500", "", "浙江", "公办", "否", "否"},
	}
	got, err := parseMajorScoreRows(rows)
	if err != nil {
		t.Fatal(err)
	}
	if len(got) != 2 {
		t.Fatalf("解析到 %d 行，想要 2（综合·收录批次·含位次）", len(got))
	}
	if got[0].SchoolName != "浙江大学" || got[0].MinRank != 1200 || got[0].Track != "综合" {
		t.Errorf("第一行 = %+v", got[0])
	}
	if got[0].MinScore != 660 || got[0].MaxScore != 0 {
		t.Errorf("浙江无最高分，MaxScore 应为 0；行 = %+v", got[0])
	}
	if got[0].SelKe != "物理、化学(2科必选)" {
		t.Errorf("选科要求 = %q", got[0].SelKe)
	}
}

func TestBatchKeep(t *testing.T) {
	keep := []string{"普通类一段", "普通类二段", "普通类提前批", "平行录取一段", "平行录取二段", "普通类平行录取"}
	for _, b := range keep {
		if !batchKeep(b) {
			t.Errorf("batchKeep(%q) = false，想要 true", b)
		}
	}
	drop := []string{"高校专项计划批", "艺术类本科批", "体育类专科批", ""}
	for _, b := range drop {
		if batchKeep(b) {
			t.Errorf("batchKeep(%q) = true，想要 false", b)
		}
	}
}
