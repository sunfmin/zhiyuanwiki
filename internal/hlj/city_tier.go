var cityStrip = strings.NewReplacer("市", "", "特别行政区", "")

// CityTier 返回城市层级（一线/新一线/二线/三线）；裸名匹配（去常见后缀）。未知返回 ""。
func CityTier(city string) string {
	c := cityStrip.Replace(strings.TrimSpace(city))
	for _, suf := range []string{"朝鲜族自治州", "回族自治州", "自治州", "地区", "盟"} {
		c = strings.TrimSuffix(c, suf)
	}
	return cityTierMap[c]
}
