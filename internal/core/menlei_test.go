package core

import "testing"

func newTestMenlei() *MenleiClassifier {
	mc := NewMenleiClassifier()
	mc.addRows([][]string{
		{"年份", "学校", "门类", "专业"},
		{"2020", "某大学", "理学", "数学类（数学与应用数学、信息与计算科学）"},
		{"2020", "某大学", "管理学", "工商管理类（会计学、财务管理）"},
		{"2020", "某大学", "医学", "临床医学"},
		// 高职专科「大类」噪声行：门类不在 12 门类内，应被忽略。
		{"2020", "某专科", "装备制造大类", "工业机器人技术"},
	})
	return mc
}

func TestMenleiCode(t *testing.T) {
	mc := newTestMenlei()
	tests := []struct {
		name  string
		major string
		want  string
	}{
		{"精确-去括号基名命中", "数学类", "理"},
		{"精确-全名命中", "数学类（数学与应用数学、信息与计算科学）", "理"},
		{"精确-管理学", "工商管理类", "管"},
		{"精确-医学", "临床医学", "医"},
		{"大类噪声不污染映射-退到关键词", "工业机器人技术", "工"}, // 含「机器人/技术」→ 工
		{"关键词-工学", "智能制造工程", "工"},
		{"关键词-农学优先于医（动物医学）", "动物医学", "农"},
		{"关键词-文学优先于法（法语）", "法语", "文"},
		{"关键词-法学", "法学", "法"},
		{"关键词-经济学", "数字金融", "经"},
		{"关键词-教育学", "学前教育", "教"},
		{"未命中归其他", "星际航行玄学", "他"},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := mc.Code(tt.major); got != tt.want {
				t.Errorf("Code(%q) = %q, want %q", tt.major, got, tt.want)
			}
		})
	}
}

// TestMenleiCodeDaleiAndLanguages 覆盖 issue #23：带括号注记的大类招生名、小语种、括号噪声。
// 全国目录单一真相后，门类码只学到「具体专业→门类」（缺「XX类」聚合名），易被括号注记带偏。
func TestMenleiCodeDaleiAndLanguages(t *testing.T) {
	mc := NewMenleiClassifier()
	// 模拟全国目录：只有具体专业（无「XX类」聚合名），用于验证「剥『类』按具体专业精确命中」一路。
	mc.addRows([][]string{
		{"门类", "专业"},
		{"工学", "生物医学工程"},
		{"工学", "交通运输"},
		{"工学", "电气工程及其自动化"},
		{"理学", "地球物理学"},
	})
	tests := []struct {
		name  string
		major string
		want  string
	}{
		// 剥「类」按具体专业精确命中：不再命中括号注记里的「医学/工程管理」。
		{"生物医学工程类·括号含医学不误判医", "生物医学工程类（包含生物医学工程、医学信息工程专业）", "工"},
		{"交通运输类·目录无聚合名退到具体专业", "交通运输类", "工"},
		// 关键词只扫去括号基名：括号里的「工程管理 / 勘查技术与工程」不再带偏。
		{"土木类·括号工程管理不误判管", "土木类（含工程管理）", "工"},
		{"地球物理学类·括号技术与工程不误判工", "地球物理学类（含勘查技术与工程）", "理"},
		// 大类无目录条目、靠关键词兜底（轻工 在工关键词里）。
		{"轻工类·关键词兜底", "轻工类", "工"},
		// 小语种：全国目录无条目，靠「语」关键词归文。
		{"格鲁吉亚语", "格鲁吉亚语", "文"},
		{"立陶宛语·带括号", "立陶宛语（欧洲研究）", "文"},
		{"荷兰语", "荷兰语", "文"},
		// 信号只在括号里的实验班：去括号基名落空，退到全名关键词，仍归工/理（不掉「他」）。
		{"工科试验班·信号在括号", "工科试验班（信息科学与技术）", "工"},
		{"理科试验班·信号在括号", "理科试验班（化学）", "理"},
		// 去括号基名优先于括号噪声：软件技术的括号「UI设计」不再误判艺。
		{"软件技术·括号设计不误判艺", "软件技术（UI设计）", "工"},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := mc.Code(tt.major); got != tt.want {
				t.Errorf("Code(%q) = %q, want %q", tt.major, got, tt.want)
			}
		})
	}
}

func TestMenleiAddRowsNoColumns(t *testing.T) {
	mc := NewMenleiClassifier()
	mc.addRows([][]string{
		{"年份", "院校代码", "专业名称", "计划人数"}, // 无门类列
		{"2026", "1001", "数学类", "5"},
	})
	if mc.Len() != 0 {
		t.Errorf("Len() = %d, want 0（无门类列应跳过）", mc.Len())
	}
}
